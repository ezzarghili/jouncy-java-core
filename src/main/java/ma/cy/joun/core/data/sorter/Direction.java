/**
 * Enum used for providing sorting directions
 * <code>ASC</code> for ascending sort
 * <code>DESC</code> for descending sort
 */
public enum Direction {
  /**
   * Ascending direction (to sort from smallest to biggest element )
   */
    ASC,
  /**
   * Descending direction (to sort from smallest to biggest element)
   */
  DESC
}
